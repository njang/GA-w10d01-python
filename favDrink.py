def favDrink():
    userInput = input("What's your favorite cocktail drink?")
    if(userInput == 'Martini'): print("Excellent choice! Martini is good for your soul.")
    elif(userInput == "Daiquiri"): print("Daiquiri is my favorite too!")
    elif(userInput == "Cosmopolitan"): print("Really? Are you sure the Cosmopolitan is your favorite?")
    else: print("I have never heard of that one..")
favDrink()
